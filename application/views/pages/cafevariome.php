					<p>Variants can be released using one of the following three sharing policies (fine-grained access control in the Cafe Variome interface allows data owners to modify permissions for whole datasets down to the individual variant level):</p>
					<hr>
					<div class="alert alert-success">
						<p><strong>openAccess:</strong> like a journal, where variant records are publicly available for access</p>
					</div>
					<div class="alert alert-danger">
						<p><strong>restrictedAccess:</strong> the user requests access to variant from the data owner/submitter, the user must either belong to a pre-approved group or must request access from the data owner in order to access the variant record(s)</p>
					</div>
					<div class="alert alert-info">
						<p><strong>linkedAccess:</strong> only reports the existence of a variant and the user is linked to the source database in order to access the full record</p>
