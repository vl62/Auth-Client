				<h3>How can I get recognised for submitting my data?</h3>
				<p><strong>Identifying and citing datasets:</strong></p>
				<p>A key aim of Cafe Variome "Central" is to offer incentive/reward-based schemes to encourage data sharing with the scientific community. To enable this, variant reports submitted to the central depot can be assigned a persistent, unique and citable Digital Object Identifier (DOI: http://www.doi.org). The <a href="http://datacite.org" target="_blank">DataCite</a> consortium, is creating a new global registry for research datasets published in online digital repositories, built on the same established DOI infrastructure that is already used widely to identify and cite traditional scholarly publications.<br /><br />
					Identification and citing via data DOIs enables variant reports to be cited like journal articles, and thus the aim is to give laboratory personnel more of an incentive to use the Cafe Variome system and be credited for their contributions. Additionally Cafe Variome will help facilitate the gathering of online access statistics (using citation metrics) relating to the use and impact of the published datasets by the research community at various levels of granularity (e.g per datasets, per data creator, per diagnostic lab).</p>
				<p><strong>Contributor Recognition:</strong></p>
				<p>Cafe Variome is aiming to unambiguously track the identity of persons who contribute data to the system, for the purpose of recognising and rewarding (and hence promoting) such activities. To enable this Cafe Variome users can link their user account with a unique author/contributor identifier utilising the <a href="http://www.orcid.org" target="_blank">ORCID</a> system. As variant reports are submitted and assigned DOIs, they can be linked with the ORCID ID of the data submitter (and, optionally for his/her co-contributors). The link(s) between the DOI and the ORCID ID(s) will subsequently be deposited into the ORCID registry, thus unambiguously attributing the data publication to the person(s) who contributed to it.</p>
				<hr>
