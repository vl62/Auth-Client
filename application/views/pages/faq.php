			<section id="conditions">
				<h3>Under what conditions can I release variants?</h3>
				<p>Data can be shared (and subsequently discovered) under the following three models:</p>
				1. Open Access: like a journal, where variant records are publicly available for access<br />
				2. Restricted Access: the user requests access to variant from the data owner/submitter, the user must either belong to a pre-approved group or must request access from the data owner in order to access the variant record(s)<br />
				3. Linked Access: only reports the existence of a variant and the user is linked to the source database in order to access the full record<br /><br />
				<p>The first of these models is recommended for maximal sharing in order to benefit the widest range of third parties (<a href="#why-share">see below</a>).</p>
